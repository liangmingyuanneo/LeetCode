int rangeBitwiseAnd(int m, int n) {
    int i,sum=0,g;
    int list[32]={1,2,4,8,16,32,64,128,256,512,1024,2048,4096,8192,16384,32768,65536,131072,262144,524288,1048576,2097152,4194304,8388608,16777216,33554432,67108864,134217728,268435456,536870912,1073741824,2147483648};
    g=n-m+1;
    for(i=0;i<32 && n>=list[i];i++){
        if(g<=list[i] && ((int) m/list[i] == (int) n/list[i])){
            sum+=(list[i]&m);
        }
    }
    return sum;
}
